<script>
import { ValidationProvider, ValidationObserver } from 'vee-validate'
import {
  BButton, BForm, BFormInput, BFormGroup, BCard, BLink, BCardTitle, BCardText, BInputGroup, BInputGroupAppend, BFormCheckbox,
} from 'bootstrap-vue'
import VuexyLogo from '@core/layouts/components/Logo.vue'
import { required, email } from '@validations'
import { togglePasswordVisibility } from '@core/mixins/ui/forms'
import axios from 'axios'
import { buildServiceUrl } from '@/constants/urls'
import sso from '@/auth/sso'
import useJwt from '@/auth/jwt/useJwt'

export default {
  components: {
    // BSV
    BButton,
    BForm,
    BFormInput,
    BFormGroup,
    BCard,
    BCardTitle,
    BLink,
    VuexyLogo,
    BCardText,
    BInputGroup,
    BInputGroupAppend,
    BFormCheckbox,
    ValidationProvider,
    ValidationObserver,
  },
  mixins: [togglePasswordVisibility],
  data() {
    return {
      userEmail: '',
      password: '',
      status: '',
      // validation rules
      required,
      email,
    }
  },
  computed: {
    passwordToggleIcon() {
      return this.passwordFieldType === 'password' ? 'EyeIcon' : 'EyeOffIcon'
    },
  },
  async created() {
    const { hash } = window.location

    if (hash === '') return

    const user = await sso.parseHash(hash)
    this.setNewSession(user)
  },
  beforeMount() {
    console.log(this.$route.query)
    if (this.$route.query.autologin) {
      console.log('hola')
      this.userEmail = this.$route.query.email
      this.password = this.$route.query.password
      this.login()
    }
  },
  methods: {
    login() {
      axios.post(buildServiceUrl('/auth/login'), { email: this.userEmail, password: this.password })
        .then(response => {
          const { data } = response
          data.idTokenPayload = {}
          this.setNewSession(data)
        })
        .catch(error => {
          console.log(error)
          this.$swal.fire({
            icon: 'error',
            title: 'Oops...',
            text: 'Usuario o contrase침a incorrectos',
          })
        })
    },
    loginWithGoogle() {
      sso.loginWithGoogle()
    },
    loginWithFacebook() {
      sso.loginWithFacebook()
    },
    setNewSession(session) {
      console.log(session)
      const user = session
      useJwt.setToken(user.accessToken)
      useJwt.setRefreshToken(user.accessToken)
      user.idTokenPayload.ability = [
        {
          action: 'manage',
          subject: 'all',
        },
      ]
      user.idTokenPayload.data = [{
        id: user.id,
        role: user.role,
        type_id: user.type_id,
      },
      ]
      localStorage.setItem('userData', JSON.stringify(user.idTokenPayload))
      console.log('SESSION')
      console.log(user)
      console.log(user.idTokenPayload)
      this.$ability.update(user.idTokenPayload.ability)
      this.$router.push('/my-events')
    },
  },
}
</script>

